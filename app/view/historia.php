	<div class="descripcion-historia">
		<p> La Banda Municipal de Música de El Bonillo se creó en 1992, actuando por primera vez el 11 de Agosto de ese año en la Feria y Fiestas locales.</p>

		<p>La totalidad de sus componentes iniciaron sus estudios musicales en 1990 en el Aula de Música de la Universidad Popular, para posteriormente crear el Excmo. Ayuntamiento lo que hasta hoy es la Escuela Municipal de Música, y su banda de música. Algunos de los alumnos que desde entonces han formado parte de dicha escuela y banda, han continuado sus estudios musicales en la Universidad de Castilla-La Mancha o Conservatorios de Música, siendo algunos hoy, profesionales en sus especialidades instrumentales y docentes, tanto en centros y entidades públicas como privadas de España.</p>


		<p>En 1992, con pocos meses de funcionamiento, esta banda fue invitada por la Peña “Santa Isabel” de Alicante, para amenizar todas sus fiestas como motivo de las “Hogueras de San Juan” y gracias a la colaboración de la Peña Amigos de El Bonillo.</p>


		<p>En 1993 se inscribió en la “Guía Cultural” de la Diputación Provincial de Albacete, realizando más de treinta actuaciones por los pueblos de nuestra provincia. En 1995 inicia su participación en los “Encuentros Regionales de Bandas Civiles” de Castilla-La Mancha, recorriendo las provincias castellano-manchegas. A partir de ello, organiza y colabora con el Excmo. Ayuntamiento de El Bonillo, en los Encuentros Regionales de Bandas con sede en El Bonillo, y también, desarrollando los Festivales de Bandas “Villa de El Bonillo” con bandas invitadas de otras regiones, ya por su XX edición.</p>

		<p>En 1999 realiza un intercambio cultural con el Centro de Musical y Danza “Nazaret” en Valencia, realizando conciertos y visitas a la ciudad. En el año 2.001 participó en la muestra de tradiciones populares con motivo del “Día de El Bonillo” en la Feria Internacional de Albacete. En 2003, desarrolla un programa de intercambio con la escuela de música y orquesta de Villanueva de los Infantes (Ciudad Real).</p>

		<p>Ha participado y organizado “Conciertos Didácticos” para centros escolares, “Conciertos Benéficos”, “Pregones Institucionales”, procesiones, pasacalles, corridas de toros, etc…, completando unas veinte actuaciones entre escuela y banda cada temporada musical en El Bonillo.</p>

		<p>Adquirido un nivel musical optimo, ha montado conciertos de solistas con Antonio Muedra trompeta de la Banda Municipal de Albacete, Luís Jaén percusionista de la Orq. Sinf. de Albacete, Antonio Tomás Martínez/Emilio José Martínez trompetas de la Banda del Cuerpo Infantería Marina y Banda Inmemorial nº 1 del Rey D. Felipe VI, Antonio Atienza Bombardino de la Escuela Municipal y Ernesto Del Amo profesor de clarinete (conciertos de trompeta de Aratjunjian, trompetas de Vivaldi, para Xilófono de Peter Tuner, Concertino para clarinete de Weber, …).</p>

		<p>En 2012, hace una presentación musical en el programa realizado en El Bonillo con la televisión pública de la región “Ancha es Castilla-La Mancha”, emitido en directo para toda la comunidad autónoma. En navidad de ese año, representa con el Grupo de Teatro Independiente de Villarrobledo “Cuento de Navidad” de Charles Dickens, con ambientación musical propia.</p>

		<p>En 2013 y 2014, realiza sendos concierto benéficos para Cruz Roja con los solistas Dña. Rosario Villar (soprano), D. Antonio Escudero (tenor) y D. José Rubio (Acordeonista), con repertorio de copla española y arias de zarzuela, tangos y pasodobles.</p>

		<p>Siendo la Semana Santa de El Bonillo declarada de Interés turístico Regional en 2011, esta banda participa anualmente en el “Pregón Religioso e Institucional”, tocando conjuntamente con una Agrupación Musical de Cornetas y tambores, con repertorio adaptado.</p>

		<p>En 2014, realiza su proyecto lúdico-musical más ambicioso, realizando un viaje a Francia, culminando su interpretación musical en el desfile oficial de DisneyLand París. En este mismo año, realiza un pasacalle por la Playa de La Concha en San Sebastián, y realiza un concierto de pasodobles en la Plaza de Cervantes (frente a dicha playa).</p>

		<p>En 2015, participa en la grabación del CD´s “Sones de Música Cofrade” en la Semana Santa de su pueblo.</p>

		<p>Esta joven agrupación es el referente musical de la escuela municipal, en la que participan más de 100 alumnos en diferentes especialidades instrumentales y ocho profesores. De la banda también nacen otras agrupaciones musicales como su Big- Band, la Brass-Band y la charanga. Constantemente su plantilla se regenera desde los jóvenes músicos de su banda juvenil, cuya plantilla media es de veinticinco educandos.</p>

		<p>Posee un amplio repertorio de obras que acredita sus más de cien conciertos realizados hasta la fecha, dentro y fuera de su pueblo.</p>

		<p>Hoy forman parte de esta banda 60 músicos, y son la sexta promoción de jóvenes músicos de El Bonillo, cuya dedicación, ilusión y proyecto musical pasa por la ampliación de conocimientos y repertorio bandístico, en el deseo de trasladar y compartir esta pasión por la Música con todos sus conciudadanos.</p>
